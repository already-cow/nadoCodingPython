#연산자
print(2**3) #2^3
print(5%3) #나머지 구하기
print(5//3) #몫 구하기
print(10 > 3) #불리언

print(3 == 3) #앞뒤가 같다
print(1 != 3) #앞뒤가 같지 않다

print(not (1 != 3)) #반대의미

print((3 > 0) and (3 < 5)) #둘 다 만족해야 true
print((3> 0 ) & (3 < 5)) #and=&

print((3 < 0) or (3 < 5)) #하나만 만족해도 true
print((3 < 0) | (3 < 5)) #or=|